def backup_and_swap():
    if BACKUP_ROOT.exists():
        shutil.rmtree(BACKUP_ROOT)
    BACKUP_ROOT.mkdir(parents=True, exist_ok=True)
    for name in ["episodes", "relations", "motifs", "L4_raw_sessions", "global_mem.txt", "global_mem_insight.txt"]:
        src = MEMORY_DIR / name
        dst = BACKUP_ROOT / name
        if src.is_dir():
            shutil.copytree(src, dst)
        else:
            shutil.copy2(src, dst)

    for name in ["episodes", "relations", "motifs"]:
        target = MEMORY_DIR / name
        if target.exists():
            shutil.rmtree(target)
        shutil.copytree(STAGING_MEMORY / name, target)

    for name in ["canon_reading", "canon_evidence"]:
        target = MEMORY_DIR / "L4_raw_sessions" / name
        if target.exists():
            shutil.rmtree(target)
        shutil.copytree(STAGING_MEMORY / "L4_raw_sessions" / name, target)

    for name in ["global_mem.txt", "global_mem_insight.txt"]:
        shutil.copy2(STAGING_MEMORY / name, MEMORY_DIR / name)


