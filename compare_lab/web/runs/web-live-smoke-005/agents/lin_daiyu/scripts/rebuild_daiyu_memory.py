def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def chunk_paragraphs(lines, group=2):
    paras = []
    for idx in range(0, len(lines), group):
        paras.append("".join(lines[idx: idx + group]))
    return "\n\n".join(paras)


def load_frontmatter_and_old_body(path: Path):
    text = read_text(path)
    m = re.match(r"^---\n(.*?)\n---\n", text, re.S)
    if not m:
        raise ValueError(f"missing frontmatter: {path}")
    frontmatter = m.group(1).strip()
    old_body = text[m.end():].strip()
    return frontmatter, old_body


def parse_source_chapters(frontmatter: str):
    m = re.search(r"source_chapters:\s*\[([^\]]*)\]", frontmatter)
    if not m:
        return []
    parts = [p.strip() for p in m.group(1).split(",") if p.strip()]
    return parts


def extract_section(text: str, heading: str) -> str:
    pattern = rf"## {re.escape(heading)}\n(.*?)(?=\n## |\Z)"
    m = re.search(pattern, text, re.S)
    if not m:
        return "- 暂缺。"
    return m.group(1).strip()


def chapter_title(source_text: str, chapter_id: str) -> str:
    first = source_text.splitlines()[0].strip()
    if first.startswith("###"):
        return first.replace("###", "", 1).strip()
    return f"第{chapter_id}回"


def render_episode(frontmatter: str, file_name: str) -> str:
    blueprint = EPISODE_BLUEPRINTS[file_name]
    source_chapters = parse_source_chapters(frontmatter)
    chapter_refs = "、".join(
        [f"`memory/L4_raw_sessions/canon_reading/{c.zfill(3)}.md` 与 `memory/L4_raw_sessions/canon_evidence/{c.zfill(3)}.md`" for c in source_chapters]
    )
    body = (
        f"---\n{frontmatter}\n---\n\n"
        "## 发生了什么\n"
        f"{chunk_paragraphs([line + ' ' for line in blueprint['what']], group=2).strip()}\n\n"
        "## 我当时如何感受和判断\n"
        f"{chunk_paragraphs([line + ' ' for line in blueprint['feel']], group=2).strip()}\n\n"
        "## 这件事留下的长期痕迹\n"
        f"{chunk_paragraphs([line + ' ' for line in blueprint['impact']], group=2).strip()}\n\n"
        "## 原文回链摘要\n"
        f"本 episode 主要由 {chapter_refs} 支撑；若需更细的章回原句、知情边界和原文定位，应先下钻对应的 `canon_reading` 与 `canon_evidence` 文件。\n"
    )
    return body


def build_chapter_to_episode_map():
    mapping = {}
    for ep_file in sorted((MEMORY_DIR / "episodes").glob("*.md")):
        frontmatter, _ = load_frontmatter_and_old_body(ep_file)
        for chapter in parse_source_chapters(frontmatter):
            mapping.setdefault(chapter.zfill(3), []).append(ep_file.name)
    return mapping


def render_canon_reading(chapter_id: str, old_note: str, source_text: str) -> str:
    title = chapter_title(source_text, chapter_id)
    relevant = extract_section(old_note, "对黛玉可入记忆的部分")
    forbidden = extract_section(old_note, "黛玉不会以凡间自觉直接知道的部分")
    supports = extract_section(old_note, "可支撑的上层记忆")
    return (
        f"# {title} 阅读全档\n\n"
        "## 来源\n"
        f"- 章节文件: `honglou/{chapter_id}.md`\n"
        f"- 章节编号: `{chapter_id}`\n\n"
        "## 知情标签摘要\n"
        "### direct_or_memory_relevant\n"
        f"{relevant}\n\n"
        "### forbidden_or_non_direct\n"
        f"{forbidden}\n\n"
        "### upward_support\n"
        f"{supports}\n\n"
        "## 原文全文\n\n"
        f"{source_text.strip()}\n"
    )


def render_canon_evidence(chapter_id: str, old_evidence: str, chapter_to_episodes) -> str:
    related = chapter_to_episodes.get(chapter_id, [])
    related_lines = "\n".join([f"- `episodes/{name}`" for name in related]) or "- 本回暂无直接 episode 对应。"
    clean_old = old_evidence.strip()
    if not clean_old:
        clean_old = "- 暂无旧版锚点；本回请直接从 `canon_reading` 原文全文中定位。"
    return (
        f"# 第{chapter_id}回 证据档\n\n"
        "## 本回对应的上层记忆\n"
        f"{related_lines}\n\n"
        "## 旧版证据锚点（保留并扩展）\n"
        f"{clean_old}\n\n"
        "## 使用说明\n"
        "- 回想细节时，先回看本回 `canon_reading` 的原文全文。\n"
        "- 需要证明某句判断来自哪里时，再用本文件里的锚点回链 episode / relation / motif。\n"
        "- 若锚点仍不足，以 `canon_reading` 原文全文为最高保真来源。\n"
    )


